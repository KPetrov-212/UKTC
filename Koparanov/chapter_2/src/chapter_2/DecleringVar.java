package chapter_2;

public class DecleringVar {

	public static void main(String[] args) {
        byte a = 97;
        short b = -115;
        int c = 52130;
        int d = 4825932;
        short e = -10000;
        
        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);
        System.out.println("d: " + d);
        System.out.println("e: " + e);
	}
}
